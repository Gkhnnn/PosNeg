package alistirmalar1;

public class BackSlash {
    public static void main(String[] args) {
        System.out.println(123456);
        System.out.println("123456");


        System.out.println("Ispanya\tItalya\bDanimarka\nAlmanya");
        System.out.println("Ali \'Merhaba\' dedi."); // çift veya tek tırnak için \ backslash kullanılır ve ardından tırnaklaar bırakılır.
        System.out.println("\"Merhaba\" = " + "Merhaba");


    }
}
