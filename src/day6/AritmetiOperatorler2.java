package day6;

public class AritmetiOperatorler2 {
    public static void main(String[] args) {
        // 10 u 3e böldügümüz de kalan 1 olarak verir.
        System.out.println("10%3=" + 10 % 3);
        System.out.println("44%2=" + 44 % 2);// kalan çift ise çifttir.
        System.out.println("13%2=" + 13 % 2);// kalan 1ve 13 ise sayı tektir.

        // % moduldu veya remainder.
        System.out.println("2345%35="+ 2345 % 35);
        // bir sayının tek çift olup olmadıgını bulmak veya asal ayıları bulmak için kullanılır.



    }
}
